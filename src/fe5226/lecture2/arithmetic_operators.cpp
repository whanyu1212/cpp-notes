#include <iostream>

int main()
{
    int a = 24;
    int b = 5;

    std::cout << "a + b = " << a + b << std::endl; // possible to replace endl with "\n"
    std::cout << "a - b = " << a - b << std::endl;
    std::cout << "a * b = " << a * b << std::endl;
    std::cout << "a / b = " << a / b << std::endl;
    std::cout << "a % b = " << a % b << std::endl;

    return 0;
}