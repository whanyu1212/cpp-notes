#include <iostream>
using namespace std;

int main()
{
    int x = 10;
    int y = 20;

    // prints either 1 or 0 to represent true or false

    cout << "x == y: " << (x == y) << "\n";
    cout << "x != y: " << (x != y) << "\n";
    cout << "x > y: " << (x > y) << "\n";
    cout << "x < y: " << (x < y) << "\n";
    cout << "x >= y: " << (x >= y) << "\n";
    cout << "x <= y: " << (x <= y) << "\n";

    return 0;
}