#include <iostream>
#include <cmath> // math library in C++

int main()
{
    double x = 3;
    double y = 4;

    std::cout << "The maximum of x and y is: " << std::max(x, y) << std::endl;
    std::cout << "x raised to the power of y is: " << pow(x, y) << std::endl;
    std::cout << "The square root of x is: " << sqrt(x) << std::endl;

    return 0;
}